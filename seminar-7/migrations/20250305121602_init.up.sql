-- Создаём таблицы
CREATE TABLE Countries (
    country_id VARCHAR(3) PRIMARY KEY,
    name VARCHAR(40) NOT NULL,
    area_sqkm INTEGER,
    population INTEGER
);

CREATE TABLE Olympics (
    olympic_id VARCHAR(7) PRIMARY KEY,
    country_id VARCHAR(3) NOT NULL,
    city VARCHAR(50) NOT NULL,
    year INTEGER NOT NULL,
    startdate DATE NOT NULL,
    enddate DATE NOT NULL,
    CONSTRAINT fk_olympics_country FOREIGN KEY (country_id) REFERENCES Countries(country_id) ON DELETE CASCADE
);

CREATE TABLE Players (
    player_id VARCHAR(10) PRIMARY KEY,
    name VARCHAR(40) NOT NULL,
    country_id VARCHAR(3) NOT NULL,
    birthdate DATE NOT NULL,
    CONSTRAINT fk_players_country FOREIGN KEY (country_id) REFERENCES Countries(country_id) ON DELETE CASCADE
);

CREATE TABLE Events (
    event_id VARCHAR(7) PRIMARY KEY,
    name VARCHAR(40) NOT NULL,
    eventtype VARCHAR(20) NOT NULL,
    olympic_id VARCHAR(7) NOT NULL,
    is_team_event BOOLEAN NOT NULL,
    num_players_in_team INTEGER,
    result_noted_in VARCHAR(100),
    CONSTRAINT fk_events_olympics FOREIGN KEY (olympic_id) REFERENCES Olympics(olympic_id) ON DELETE CASCADE
);

CREATE TABLE Results (
    event_id VARCHAR(7) NOT NULL,
    player_id VARCHAR(10) NOT NULL,
    medal VARCHAR(7),
    result FLOAT,
    CONSTRAINT fk_results_event FOREIGN KEY (event_id) REFERENCES Events(event_id) ON DELETE CASCADE,
    CONSTRAINT fk_results_player FOREIGN KEY (player_id) REFERENCES Players(player_id) ON DELETE CASCADE
);


